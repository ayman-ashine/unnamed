function generate_block(position) {

    let block = []

    const hash = String(MD5(`${position[0]}${position[1]}`))

    if (hash.includes("000"))
        block = UI_OBJECTS["tree"]
    else if (hash.includes("111"))
        block = UI_OBJECTS["flower-green"]
    else if (hash.includes("2222"))
        block = UI_OBJECTS["flower-red"]
    else if (hash.includes("0")) block = [...hash].map(c => {
        return COLORS[c]
    });
    else
        block = UI_OBJECTS["space"]

    return block

}

